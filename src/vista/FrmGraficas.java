    private void btnEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEjecutarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel miModelo;
        GraficaComida miGrafica = new GraficaComida();
        int op = cmbConsultas.getSelectedIndex();
        switch(op){
           /* case 1:
                miModelo = miGrafica.mostrarComida ();
                tblConsultas.setModel(miModelo);
                break;*/
            case 1:
                if(jFechaD.getDate()!=null && jFechaH.getDate()!=null){
                SimpleDateFormat formatoFechaD = new SimpleDateFormat("yyyy-MM-dd");
                SimpleDateFormat formatoFechaH = new SimpleDateFormat("yyyy-MM-dd");
                String fechaD= formatoFechaD.format(jFechaD.getDate());
                String fechaH= formatoFechaH.format(jFechaH.getDate());
                miModelo = miGrafica.agruparComida(fechaD, fechaH);
                tblConsultas.setModel(miModelo); 
                }else{
                JOptionPane.showMessageDialog(rootPane, "Ingrese intervalo de fechas por favor");
                }
                
                break;
            /*case 3:
                miModelo = miGrafica.mostrarPlatos();
                tblConsultas.setModel(miModelo); 
                break; */
            default: JOptionPane.showMessageDialog(rootPane, "Selecciona una consulta");
        }
        
    }//GEN-LAST:event_btnEjecutarActionPerformed

    private void btnGraficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficarActionPerformed
        // TODO add your handling code here:   
      if((rb3D.isSelected()||rb2D.isSelected())&&(rbBarra.isSelected()||rbCircular.isSelected())){
        if(rb3D.isSelected() && rbBarra.isSelected()){
            DefaultCategoryDataset dtsc = new DefaultCategoryDataset();
            
            for(int i = 0; i < tblConsultas.getRowCount(); i++){
                dtsc.addValue(Integer.parseInt(tblConsultas.getValueAt(i, 1).toString()), tblConsultas.getValueAt(i, 0).toString(),tblConsultas.getValueAt(i, 0).toString());
            }
            JFreeChart ch = ChartFactory.createBarChart3D("Platos m√°s vendidos", "platos", "cantidad", dtsc,PlotOrientation.VERTICAL, true, true, false);
            ChartPanel cp = new ChartPanel(ch);
            add(cp);
            cp.setBounds(500,70,500,400);
            
        }
        if(rb2D.isSelected() && rbBarra.isSelected()){
                DefaultCategoryDataset dtsc = new DefaultCategoryDataset();
            
            for(int i = 0; i < tblConsultas.getRowCount(); i++){
                dtsc.addValue(Integer.parseInt(tblConsultas.getValueAt(i, 1).toString()), tblConsultas.getValueAt(i, 0).toString(),tblConsultas.getValueAt(i, 0).toString());
            }
            JFreeChart ch = ChartFactory.createBarChart("Platos m√°s vendidos", "platos", "cantidad", dtsc,PlotOrientation.VERTICAL, true, true, false);
            ChartPanel cp = new ChartPanel(ch);
            add(cp);
            cp.setBounds(500,70,500,400);
        
            
        }        
        
        if(rb3D.isSelected() && rbCircular.isSelected()){
            
            DefaultPieDataset dtsc = new DefaultPieDataset();
            
            for(int i = 0; i < tblConsultas.getRowCount(); i++){
                dtsc.setValue(tblConsultas.getValueAt(i, 0).toString(), Integer.parseInt(tblConsultas.getValueAt(i, 1).toString()));
            }
            JFreeChart ch = ChartFactory.createPieChart3D("Platos m√°s vendidos", dtsc,true, true, false);
            ChartPanel cp = new ChartPanel(ch);
            add(cp);
            cp.setBounds(500,70,500,400);
            
              
         }
        if(rb2D.isSelected() && rbCircular.isSelected()){
            DefaultPieDataset dtsc = new DefaultPieDataset();
            
            for(int i = 0; i < tblConsultas.getRowCount(); i++){
                dtsc.setValue(tblConsultas.getValueAt(i, 0).toString(), Integer.parseInt(tblConsultas.getValueAt(i, 1).toString()));
            }
            JFreeChart ch = ChartFactory.createPieChart("Platos m√°s vendidos", dtsc,true, true, false);
            ChartPanel cp = new ChartPanel(ch);
            add(cp);
            cp.setBounds(500,70,500,400);
            
        }
        
      }else {
       JOptionPane.showMessageDialog(rootPane,"seleccione tipo de gr√°fica y la dimensi√≥n");
      }  
        
    }//GEN-LAST:event_btnGraficarActionPerformed

